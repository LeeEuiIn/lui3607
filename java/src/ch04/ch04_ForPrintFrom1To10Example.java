package ch04;

public class ch04_ForPrintFrom1To10Example {
	
	public static void main(String[] args) {
		for(int i=1; i<=10; i++) { // for은 초기화식 , 조건식 , 증감식 으로 구성된다.
			//정수형 int을 i으로 지정하며 숫자1로 설정
			// 1부터 10까지의 숫자 범위를 지정
			// 1부터 차례대로 1씩 증감식을 지정 
			System.out.println(i);	//차례대로 숫자를 출력한다.	
		}
	}

}
