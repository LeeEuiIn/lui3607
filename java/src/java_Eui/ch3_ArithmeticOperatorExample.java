package java_Eui;

public class ch3_ArithmeticOperatorExample {
	public static void main(String[] args) {
		int v1 = 5;
		int v2 = 2;
		
		int result1 = v1 + v2;
		System.out.println("result1=" + result1);//7
		
		int result2 = v1 - v2;
		System.out.println("result2=" + result2);//3
		
		int reuslt3 = v1 * v2;
		System.out.println("result3=" + reuslt3);
		
		int reuslt4 = v1 / v2;
		System.out.println("result4=" + reuslt4);
		
		int reuslt5 = v1 % v2;
		System.out.println("result5=" + reuslt5);
		
		double reuslt6 = (double) v1 / v2;
		System.out.println("result6=" + reuslt6);
	}

}
